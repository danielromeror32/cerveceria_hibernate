    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        int ide = (int) jComboBox1.getSelectedItem();
        Expendio exp;
        int idf = (int) jComboBox2.getSelectedItem();
        Fabricante fab;
        int idc = (int) jComboBox3.getSelectedItem();
        Cerveza cer;
        
        if(jTextField2.getText().equals("")|| jTextField3.getText().equals("")||
        jTextField4.getText().equals("")||jTextField5.getText().equals("")){
           JOptionPane.showMessageDialog(this, "Ingrese todos los datos", "Error", JOptionPane.ERROR_MESSAGE);
      }else{
            
           exp= em.findExpendioid(ide);
           fab= fm.findFabricanteid(idf);
           cer = cm.findCervezaid(idc);
           
           Pedido ObjPed = new Pedido(exp,fab,cer,Integer.parseInt(jTextField2.getText()),
                   jTextField3.getText(), jTextField4.getText(),Float.parseFloat(jTextField5.getText()) );
           ObjPed.setIdpedido(Integer.parseInt(label_id.getText()));
         
         JDPed2 ventana = new JDPed2(ObjPed);
          this.setVisible(false);
         ventana.setVisible(true);
      
      }
        
    }//GEN-LAST:event_jButton2ActionPerformed

