    public void agregaExp(){
        List ids;
        
        ids = em.listidexp();
        Iterator it = ids.iterator();
        while (it.hasNext()) {
            jComboBox1.addItem(it.next());
        }
        
    }
    
    public void agregaFab(){
        List ids;
        
        ids = fm.listidfa();
        Iterator it = ids.iterator();
        while (it.hasNext()) {
            jComboBox2.addItem(it.next());
        }
    }
    public void agregaCer(){
        List ids;
        
        ids = cm.listidce();
        Iterator it = ids.iterator();
        while (it.hasNext()) {
            jComboBox3.addItem(it.next());
        }  
    }
