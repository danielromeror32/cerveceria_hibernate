    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

     
        if ( op.getIdexpendio() <= ultimo) {
            
            em.updateExpendio(op.getIdexpendio(), op.getNombre(), op.getRfc(), op.getResponsable(), 
                    op.getEstado(), op.getMunicipio(), op.getUbicacion());
            JOptionPane.showMessageDialog(this, "Se ha actualizado fabrica correctamente");
       
        }else{
           
            em.createAndStoreExp(op.getNombre(), op.getRfc(), op.getResponsable(),op.getEstado(), 
                    op.getMunicipio(), op.getUbicacion());
            
        }
       
        
    JFInicio ventana = new JFInicio();
        ventana.setVisible(true);
        this.dispose();
      
    }//GEN-LAST:event_jButton1ActionPerformed

